// @lc code=start
/**
 * @param {string} s
 * @param {number} numRows
 * @return {string}
 */
var convert = function(s, numRows) {
    if (numRows <= 1) return s;
    let resultsArray = new Array(numRows).fill("");
    let pattern = 2 * numRows - 2;
    for (let i = 0; i < s.length; i++) {
      let row = i % pattern;
      row = (row < numRows) ? row : pattern - row;
      resultsArray[row] += s[i];
    }
    return resultsArray.join('');
};
